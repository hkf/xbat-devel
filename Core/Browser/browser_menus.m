function browser_menus(par, names)

menu_names = { ...
	'file', ...
	'edit', ...
	'view', ...
	'sound', ...
	'filter', ...
	'feature', ...
	'detect', ...
	'log', ...
	'annotate', ...
	'measure', ...
	'window', ...
	'help' ...
};

if nargin < 2 || isempty(names)
	names = menu_names;
else
	names = intersect(lower(names), menu_names);
end

for k = 1:length(names)
	
	fcn = ['browser_', names{k}, '_menu'];
	
	if isempty(which(fcn))
		continue;
	end
	
	fun = str2func(fcn); fun(par);
	
end
