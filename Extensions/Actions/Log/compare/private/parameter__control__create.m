function control = parameter__control__create(parameter, context)

% COMPARE - parameter__control__create

control = empty(control_create);

log = get_library_logs('logs', [], context.sound, context.target{1})


control(end + 1) = control_create( ...
    'name', 'reference_log', ...
    'style', 'popup', ...
    'string', get_library_logs('info', [], log.sound) ...
);

control(end + 1) = control_create( ...
    'name', 'event_overlap_threshold', ...
    'style', 'slider', ...
    'value', parameter.event_overlap_threshold ...
);

control(end + 1) = control_create( ...
    'name', 'low_score', ...
    'style', 'slider', ...
    'min', 0, ...
    'max', 10, ...
    'value', parameter.low_score ...
);

control(end + 1) = control_create( ...
    'name', 'high_score', ...
    'style', 'slider', ...
    'min', 0, ...
    'max', 10, ...
    'value', parameter.high_score ...
);

control(end + 1) = control_create( ...
    'name', 'n_points', ...
    'style', 'slider', ...
    'min', 1, ...
    'max', 100, ...
    'value', parameter.n_points ...
);

